class Solution {
  public:
    vector<int> intersectionWithDuplicates(vector<int>& a, vector<int>& b) {
        vector<int>res;
        map<int, int>mp;
        set<int>seen;
        for(auto &vals: a) mp[vals]++;
        for(auto &vals: b){
            if(seen.find(vals)==seen.end()){
                if(mp.find(vals)!=mp.end()){
                    res.push_back(vals);
                    seen.insert(vals);
                }  
            }
        }
        return res;
    }
};

